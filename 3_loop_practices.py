# Vòng lặp for với list
fruits = ["apple", "banana", "orange"]
print("=== For loop with list ===")
for fruit in fruits:
    print(f"I like {fruit}")

# Vòng lặp for với range
print("\n=== For loop with range ===")
for i in range(5):
    print(f"Number: {i}")

# Vòng lặp while
print("\n=== While loop ===")
count = 0
while count < 3:
    print(f"Count: {count}")
    count += 1

